public class Arrays {
    public static void main(String[] args) {
        int[] n={1,2,3,4,5,6};
        int[] m={7,8,9};
        int[] o={10,11,12};
        //Enhanced For Loop
        for(int num : n){
            System.out.println(num);
        }
        for(int num : m){
            System.out.println(num);
        }
        for(int num : o){
            System.out.println(num);
        }
    }
}
